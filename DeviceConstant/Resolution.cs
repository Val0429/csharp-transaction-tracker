    public static class Resolutions
    {
        public static Resolution ToIndex(String value)
        {
            foreach (KeyValuePair<Resolution, String> keyValuePair in List)
            {
                if (String.Equals(value, keyValuePair.Value))
                    return keyValuePair.Key;
            }

            if (String.Equals(value, "0x0"))
                return Resolution.NA;

            return 0;
        }

        public static UInt16 ToWidth(this Resolution index)
        {
            String value = ToString(index);

            if(value.IndexOf("-") > -1)
            {
                value = value.Split('-')[0];
            }

            if (value != "" && value != "N/A")
                return Convert.ToUInt16(value.Split('x')[0]);

            return 0;
        }

        public static UInt16 ToHeight(this Resolution index)
        {
            String value = ToString(index);

            if (value.IndexOf("-") > -1)
            {
                value = value.Split('-')[0];
            }

            if (value != "" && value != "N/A")
                return Convert.ToUInt16(value.Split('x')[1]);

            return 0;
        }

        public static String ToString(Resolution index)
        {
            foreach (KeyValuePair<Resolution, String> keyValuePair in List)
            {
                if (index == keyValuePair.Key)
                    return keyValuePair.Value;
            }

            return "";
        }

